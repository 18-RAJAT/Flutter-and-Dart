void main()
{
  String grade = 'B';
  switch(grade)
  {
    case 'A':
    print("Awesome");
    break;

    case 'B':
    print("Nice");
    break;

    case 'C':
    print("Work hard!");
    break;
    
    case 'D':
    print("Failed");
    break;

    default:
    print("Invalid Grade");
  }
}