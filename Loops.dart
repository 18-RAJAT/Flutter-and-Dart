
/*
   Loops 1.Definite ->>For loop
         2.Indefinite ->> while loop, Do - While loop
*/

//For loop
void main()
{
  int i=0;
  for(int i=0;i<4;++i)
  {
    print("Hello");
  }
  print("\n");
  {
    print("exit");
  }
}


//While loop

void main()
{
  int i=0;
  while(i<5)
  {
    print("Hello");
    i++;
  }
}



//Do While

void main()
{
  int i=0;
  do{
    print("Hello");
    i++;
  }

  while(i<5);
}

